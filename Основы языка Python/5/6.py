"""Необходимо создать (не программно) текстовый файл,
где каждая строка описывает учебный предмет и наличие лекционных,
практических и лабораторных занятий по этому предмету и их количество.
Важно, чтобы для каждого предмета не обязательно были все типы занятий.
Сформировать словарь, содержащий название предмета и
общее количество занятий по нему. Вывести словарь на экран.
Примеры строк файла:
Информатика: 100(л) 50(пр) 20(лаб).
Физика: 30(л) — 10(лаб)
Физкультура: — 30(пр) —
Пример словаря:
{“Информатика”: 170, “Физика”: 40, “Физкультура”: 30}"""

import shutil
import sys

subj = {}
with open('text_6.txt', 'r', encoding='utf-8') as f_obj:
    shutil.copyfileobj(f_obj, sys.stdout)
    f_obj.seek(0)
    print()
    for line in f_obj:
        line = (line.replace('—', '0').replace('.', '').replace(':', '')
                .replace('(л)', '').replace('(пр)', '').replace('(лаб)', ''))
        subject, lecture, practice, lab = line.split()
        subj[subject] = int(lecture) + int(practice) + int(lab)
    print(f'Общее количество часов по предмету - \n {subj}')

